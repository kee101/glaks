/**
 * Provides methods to respond to user interaction in the Main Menu.
 */
public interface MainMenuViewListener {

	/**
	 * Method to be called when the Exit button is clicked.
	 */
	void onExitButtonClicked();

	/**
	 * Method to be called when the Settings button is clicked.
	 */
	void onSettingsButtonClicked();

}
